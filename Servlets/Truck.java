package assessment;

public class Truck {
	int id;
	String owner;
    String location;
	
	public Truck() {
		
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public void setOwner(String owner) {
		this.owner = owner;
	}
	
	public void setLocation(String location) {
		this.location = location;
	}
	
	public int getId() {
		return id;
	}
	
	public String getOwner() {
		return owner;
	}
	
	public String getLocation() {
		return location;
	}
	
}
